package Day3.modified_operators;

public class BitWiseOp {
    public static void main(String[] args) {

        int num1=6;
        int num2=4;

        //  Bitwise And (&)
        int result=num1&num2;
        System.out.println(result);

        // Inclusive Or ( | )
        int a=num1|num2;
        System.out.println(a);

        // Exclusive Or (^)
        int b=num1^num2;
        System.out.println(b);

        // Compliment (~)

    }
}
