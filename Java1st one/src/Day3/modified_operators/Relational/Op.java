package Day3.modified_operators.Relational;

public class Op {
    public static void main(String[] args) {
        int num1=21;
        int num2=42;
        System.out.println(num1>num2);
        System.out.println(num1<num2);
        System.out.println(num1>=num2);
        System.out.println(num1<=num2);
        System.out.println(num1==num2);
        System.out.println(!(num1!=num2));
    }
}
