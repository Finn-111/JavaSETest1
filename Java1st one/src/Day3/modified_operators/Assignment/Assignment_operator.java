package Day3.modified_operators.Assignment;



public class Assignment_operator {
    public static void main(String[] args) {
        int a=5;

         //a=a+8;
        a+=8;
        System.out.println(a);
        a-=8;
        System.out.println(a);
        a*=8;
        System.out.println(a);
        a/=8;
        System.out.println(a);
        a%=8;
        System.out.println(a);
    }
}

