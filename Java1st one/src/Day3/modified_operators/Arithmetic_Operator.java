package Day3.modified_operators;

public class Arithmetic_Operator {
    public static void main(String[] args) {

        int num1=23;
        int num2=12;

        System.out.println(num1*num2);

        System.out.println(num1/num2);

        System.out.println(num1%num2);

        System.out.println(num1+num2);

        System.out.println(num1-num2);
                                             }
}