package day2.operators;

public class Arithmetic_Op {
    public static void main(String[] args) {
        int a=10;
        int b=2;

        int plus=a+b;
        System.out.println(plus);
        int minus=a-b;
        System.out.println(minus);
        int mut=a*b;
        System.out.println(mut);
        int div=a/b;
        System.out.println(div);
        int modu=a%b;
        System.out.println(modu);


    }
}
