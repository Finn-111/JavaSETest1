package day2.operators;

public class Logical_Op {
    public static void main(String[] args) {

        boolean bool1 = true, bool2 = false;

        //AND &&
        System.out.println(bool1 & bool2);

        // ||
        System.out.println(bool1 || bool2);

        // Not !
        System.out.println(!(bool1 && bool2));
    }}
